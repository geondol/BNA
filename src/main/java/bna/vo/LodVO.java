package bna.vo;

public class LodVO {
	private int lodnum,lodprice;
	private String lodname,lodimg1,lodinfo,lodaddr;
	public int getLodnum() {
		return lodnum;
	}
	public void setLodnum(int lodnum) {
		this.lodnum = lodnum;
	}
	public int getLodprice() {
		return lodprice;
	}
	public void setLodprice(int lodprice) {
		this.lodprice = lodprice;
	}
	public String getLodname() {
		return lodname;
	}
	public void setLodname(String lodname) {
		this.lodname = lodname;
	}
	public String getLodimg1() {
		return lodimg1;
	}
	public void setLodimg1(String lodimg1) {
		this.lodimg1 = lodimg1;
	}
	public String getLodinfo() {
		return lodinfo;
	}
	public void setLodinfo(String lodinfo) {
		this.lodinfo = lodinfo;
	}
	public String getLodaddr() {
		return lodaddr;
	}
	public void setLodaddr(String lodaddr) {
		this.lodaddr = lodaddr;
	}
	
	
}
